    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      // IMPLEMENTASI EXCEPTION HANDLING
        try {
            String judul = txtJudul.getText();
            String jurnalis = txtJurnalis.getText();
            String infoKhusus = txtInfoKhusus.getText(); 
            
            // Mencegah NullPointerException jika dropdown bermasalah
            if (cmbJenis.getSelectedItem() == null) {
                throw new NullPointerException("Pilihan jenis berita tidak terbaca!");
            }
            String jenisTerpilih = cmbJenis.getSelectedItem().toString();

            // Melempar Exception buatan (IllegalArgumentException) jika ada form yang kosong
            if (judul.isEmpty() || jurnalis.isEmpty() || infoKhusus.isEmpty()) {
                throw new IllegalArgumentException("Data belum lengkap, mohon isi semua kolom!");
            }

            // --- PROSES MENYIMPAN KE TABEL (Tetap menggunakan Polimorfisme) ---
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblBerita.getModel();
            Berita berita; 

            if (jenisTerpilih.equals("Berita Turnamen")) {
                berita = new BeritaTurnamen(); 
            } else {
                berita = new BeritaTransfer(); 
            }
            
            berita.setBerita("-", judul, infoKhusus); 
            model.addRow(new Object[]{berita.getJenisBerita(), berita.judul, jurnalis, berita.game});
            
            // Membersihkan form otomatis
            txtJudul.setText("");
            txtJurnalis.setText("");
            txtInfoKhusus.setText("");
            cmbJenis.setSelectedIndex(0);
            txtJudul.requestFocus();

        } catch (IllegalArgumentException e) {
            // POP-UP EXCEPTION 1: Menangkap error form kosong
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Peringatan Input", javax.swing.JOptionPane.WARNING_MESSAGE);
            
        } catch (Exception e) {
            // POP-UP EXCEPTION 2: Menangkap error sistem lainnya yang tidak terduga
            javax.swing.JOptionPane.showMessageDialog(this, "Terjadi kesalahan sistem: " + e.getMessage(), "Error Kesalahan", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // Mengambil model dari tabel
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblBerita.getModel();
        
        // Mendapatkan index baris yang di-klik user
        int barisTerpilih = tblBerita.getSelectedRow();

        if (barisTerpilih >= 0) {
            // Hapus baris jika ada yang dipilih
            model.removeRow(barisTerpilih);
        } else {
            // Tampilkan pesan error jika user belum memilih baris
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris di tabel yang ingin dihapus terlebih dahulu.");
        }
    }//GEN-LAST:event_btnHapusActionPerformed


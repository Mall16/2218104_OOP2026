    private void btnCekCastingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekCastingActionPerformed
        int barisTerpilih = tblBerita.getSelectedRow();
        
        if (barisTerpilih < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih salah satu baris data pada tabel terlebih dahulu!");
            return;
        }
        
        // Mengambil data dari tabel
        String jenis = tblBerita.getValueAt(barisTerpilih, 0).toString();
        String judul = tblBerita.getValueAt(barisTerpilih, 1).toString();
        String game = tblBerita.getValueAt(barisTerpilih, 3).toString();
        
        // 1. IMPLEMENTASI UPCASTING
        // Objek dari kelas anak (BeritaTurnamen/BeritaTransfer) dimasukkan ke variabel kelas induk (Berita)
        Berita beritaInduk;
        
        if (jenis.equals("Hasil Turnamen")) {
            beritaInduk = new BeritaTurnamen(); // Upcasting otomatis
        } else {
            beritaInduk = new BeritaTransfer(); // Upcasting otomatis
        }
        
        beritaInduk.setBerita("-", judul, game);
        
        // 2. IMPLEMENTASI DOWNCASTING & INSTANCEOF
        // Mengecek tipe objek asli dan melakukan casting kembali ke kelas anak secara eksplisit
        if (beritaInduk instanceof BeritaTurnamen) {
            BeritaTurnamen bTurnamen = (BeritaTurnamen) beritaInduk; // DOWNCASTING eksplisit
            
            // Mengakses method spesifik milik kelas anak setelah downcasting
            String pesanSpesifik = bTurnamen.dapatkanBonusJuara();
            javax.swing.JOptionPane.showMessageDialog(this, "Berita Terseleksi: " + bTurnamen.judul + "\n" + pesanSpesifik);
            
        } else if (beritaInduk instanceof BeritaTransfer) {
            BeritaTransfer bTransfer = (BeritaTransfer) beritaInduk; // DOWNCASTING eksplisit
            
            // Mengakses method spesifik milik kelas anak setelah downcasting
            String pesanSpesifik = bTransfer.dapatkanDetailKontrak();
            javax.swing.JOptionPane.showMessageDialog(this, "Berita Terseleksi: " + bTransfer.judul + "\n" + pesanSpesifik);
        }
    }//GEN-LAST:event_btnCekCastingActionPerformed

